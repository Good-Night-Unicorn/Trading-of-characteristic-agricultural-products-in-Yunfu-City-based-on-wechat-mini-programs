				tupian: '',
				jianjie: '',
				chandi: '',
				caizhairiqi: '',
				shangjiazhanghao: '',
				shangjiaxingming: '',
					onelimittimes: Number('-1') ,
					alllimittimes: Number('-1') ,
				discussnum: '',
				totalscore: '',
				price: '',
				onshelves: '',
				storeupnum: '',
				},
				chanpinleixingOptions: [],
				chanpinleixingIndex: 0,
				// 登录用户信息
				user: {},
				ro:{
				   chanpinmingcheng : false,
				   chanpinleixing : false,
				   tupian : false,
				   jianjie : false,
				   chandi : false,
				   caizhairiqi : false,
				   shangjiazhanghao : false,
				   shangjiaxingming : false,
				   onelimittimes : false,
				   alllimittimes : false,
				   thumbsupnum : false,
				   crazilynum : false,
				   clicktime : false,
				   discussnum : false,
				   totalscore : false,
				   price : false,
				   onshelves : false,
				   storeupnum : false,
				},
				virtualPay: false,
			}
		},
		components: {
			wPicker,
			xiaEditor,
			multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			if(options.virtualPay){
				this.virtualPay = true
			}
			this.ruleForm.caizhairiqi =  this.$utils.getCurDateTime();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.shangjiazhanghao = this.user.shangjiazhanghao
			this.ro.shangjiazhanghao = true;
			this.ruleForm.shangjiaxingming = this.user.shangjiaxingming
			this.ro.shangjiaxingming = true;


			// 下拉框
			res = await this.$api.option(`chanpinleixing`,`chanpinleixing`,{});
			this.chanpinleixingOptions = res.data;
			this.chanpinleixingOptions.unshift("请选择产品类型");

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`nongchanpin`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='chanpinmingcheng'){
						this.ruleForm.chanpinmingcheng = obj[o];
						this.ro.chanpinmingcheng = true;
						continue;
					}
					if(o=='chanpinleixing'){
						this.ruleForm.chanpinleixing = obj[o];
						this.ro.chanpinleixing = true;
						continue;
					}
					if(o=='tupian'){
						this.ruleForm.tupian = obj[o].split(",")[0];
						this.ro.tupian = true;
						continue;
					}
					if(o=='jianjie'){
						this.ruleForm.jianjie = obj[o];
						this.ro.jianjie = true;
						continue;
					}
					if(o=='chandi'){
						this.ruleForm.chandi = obj[o];
						this.ro.chandi = true;
						continue;
					}
					if(o=='caizhairiqi'){
						this.ruleForm.caizhairiqi = obj[o];
						this.ro.caizhairiqi = true;
						continue;
					}
					if(o=='shangjiazhanghao'){
						this.ruleForm.shangjiazhanghao = obj[o];
						this.ro.shangjiazhanghao = true;
						continue;
					}
					if(o=='shangjiaxingming'){
						this.ruleForm.shangjiaxingming = obj[o];
						this.ro.shangjiaxingming = true;
						continue;
					}
					if(o=='onelimittimes'){
						this.ruleForm.onelimittimes = obj[o];
						this.ro.onelimittimes = true;
						continue;
					}
					if(o=='alllimittimes'){
						this.ruleForm.alllimittimes = obj[o];
						this.ro.alllimittimes = true;
						continue;
					}
					if(o=='thumbsupnum'){
						this.ruleForm.thumbsupnum = obj[o];
						this.ro.thumbsupnum = true;
						continue;
					}
					if(o=='crazilynum'){
						this.ruleForm.crazilynum = obj[o];
						this.ro.crazilynum = true;
						continue;
					}
					if(o=='clicktime'){
						this.ruleForm.clicktime = obj[o];
						this.ro.clicktime = true;
						continue;
					}
					if(o=='discussnum'){
						this.ruleForm.discussnum = obj[o];
						this.ro.discussnum = true;
						continue;
					}
					if(o=='totalscore'){
						this.ruleForm.totalscore = obj[o];
						this.ro.totalscore = true;
						continue;
					}
